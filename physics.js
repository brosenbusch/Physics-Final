function distance(v,t){
  return v*t;
}
function velocity(d,t){
  return d/t;
}
function time(d,v){
  return d/v;
}
function velocityFinal(vi,a,t){
  return vi+a*t;
}
function acceleration(v,t){
  return v/t;
}
function force(m,a){
  return m*a;
}
function forceG(G,m1,m2,r){
  return (G*m1*m2)/(r*r);
}
